int scroll_x;
int scroll_y;
char sin_idx;
char map_col;

void main() {
    irq_disable(INT_ALL);

    // set BAT size
    vdc_set_bat_size(VDC_BG_64x64);

    // set map bounds
    map_set_bat_bounds(0, vdc_bat_height);

    // load tileset palette
    vce_load_palette(0, 1, pal_00);

    // load tileset gfx
    vdc_load_data(map_8x8_tile_vram, gfx_00, 512);


    // set map pointer, tiles vram address, tiles palette id, map width and height, and wrap mode
    map_set(map_00, map_8x8_tile_vram, tile_pal_00, map_8x8_width, map_8x8_height);

    // copy map from (0,0) to (32, map_height) to BAT
    map_load(0, 0, 0, 0, 33, map_8x8_height);

    // the last map column
    map_col = 32;

    // reset index in the sine table
    sin_idx = 0;

    // set scroll windows
    scroll_set(0, 0, 120, 0, 0, VDC_CR_BG_ENABLE | 0x01);
    scroll_set(1, 120, 200, 0, 0, VDC_CR_BG_ENABLE | 0x01);
    scroll_set(2, 200, 240, 0, 256, VDC_CR_BG_ENABLE | 0x01);

    // Display a string in the area of the 3rd scroll area
    print_string(1,33,32,20, "HuDK scroll demo");

    // enable IRQ 1.
    irq_enable(INT_IRQ1);

    for(;;) {
        // wait for vsync
        vdc_wait_vsync();

        // update X scroll
        ++scroll_x;

        // this is the wavy scroll effect
        // the Y coordinate is computed like this :
        //    Y = (sin_table[sin_idx] / 2) + 64
        scroll_y = sin_table[sin_idx];
        if(scroll_y & 0x0080) {
            scroll_y |= 0xff00;
        }
        scroll_y >>= 2;
        scroll_y += 140;

        // update scroll coordinates
        scroll_set_coord(0, scroll_x, 0);
        scroll_set_coord(1, 0, scroll_y);

        ++sin_idx;     // we move to the next sine value

        // check if we need to load a map column
        // note the only go from left to right
        // and we'll never need to load more that a single column
        if((scroll_x & 7) == 0) {
            ++map_col;
            if(map_col >= map_8x8_width) {
                map_col = 0;
            }
            // copy the next map column to BAT
            map_load(map_col, 0, map_col, 0, 1, map_8x8_height);
        }
    }
}
